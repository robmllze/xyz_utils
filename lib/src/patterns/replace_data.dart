String replaceAllData(String input, Map<Pattern, dynamic> data) {
  var output = input;
  for (final entry in data.entries) {
    final pattern = entry.key;
    final value = entry.value;
    output = output.replaceAll(pattern, value.toString());
  }
  return output;
}

