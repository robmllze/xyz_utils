/// Formats the dart file at [filePath].
Future<void> fmtDartFile(String filePath) async {
  try {
    final fixedPath = toLocalPathFormat(filePath);
    await Process.run("dart", ["format", fixedPath]);
  } catch (e) {
    Here().debugLogError(e);
  }
}
