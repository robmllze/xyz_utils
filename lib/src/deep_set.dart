import 'package:collection/collection.dart' show DeepCollectionEquality;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class DeepSet<T> {
  //
  //
  //

  Set<T> values;

  //
  //
  //

  DeepSet._([this.values = const {}]);

  //
  //
  //

  @override
  bool operator ==(Object other) {
    if (identical(this, other)) return true;
    return other is DeepSet<T> && const DeepCollectionEquality().equals(other.values, values);
  }

  //
  //
  //

  @override
  int get hashCode => const DeepCollectionEquality().hash(values);
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

extension DeepSetExtension<T> on Set<T> {
  DeepSet<T> get deep => DeepSet<T>._(this);
}
