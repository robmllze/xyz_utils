class _XyzError {
  final XyzErrorType type;
  final dynamic e;
  const _XyzError(this.e, this.type);
  @override
  String toString() {
    return e.toString();
  }
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class ProgrammerError extends _XyzError {
  const ProgrammerError(dynamic e)
      : super(
          "Programmer error: $e",
          XyzErrorType.PROGRAMMER_ERROR,
        );
}

