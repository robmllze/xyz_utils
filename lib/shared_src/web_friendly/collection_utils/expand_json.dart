/// Expands a Json map, e.g. `{'a': {'b': 1}}` to `{a.b: 1, b: 1}`.
Map expandJson(
  Map input, {
  String separator = '.',
}) {
  return expandFlattenedJson(
    flattenJson(
      input,
      separator: separator,
    ),
    separator: separator,
  );
}
