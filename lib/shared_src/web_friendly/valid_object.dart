abstract class ValidObject {
  const ValidObject();
  bool get valid;
  static bool areValid(List<dynamic> inputs) {
    for (final input in inputs) {
      if (input == null) {
        return false;
      }
      try {
        if (input.isEmpty) {
          return false;
        }
      } catch (_) {}
    }
    return true;
  }
}
