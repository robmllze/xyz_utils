T cyclePlaces<T>(Iterable<T> input, [int places = 1]) {
  if (input.isEmpty) {
    throw ArgumentError("Input list should not be empty.");
  }
  final index = places % input.length; // Calculate the remainder to get a cyclic index.
  return input.elementAt(index);
}

