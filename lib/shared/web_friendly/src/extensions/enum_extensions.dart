import 'package:collection/collection.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

extension EnumFromString<T extends Enum> on Iterable<T> {
  T? valueOf(String value) {
    return this.firstWhereOrNull((type) => type.name.toLowerCase() == value.toLowerCase());
  }
}
